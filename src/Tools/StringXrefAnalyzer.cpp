// 1. Export to IDA Python script - just addresses and raw strings, no guessing
bool StringXrefAnalyzer::ExportToIDAPython(std::string& outPath) const
{
	if (m_state.rows.empty()) return false;

	std::filesystem::path baseDir = Logger::GetDefaultTempDir();
	std::error_code ec;
	std::filesystem::create_directories(baseDir, ec);

	const auto now = std::chrono::system_clock::now();
	const auto tt = std::chrono::system_clock::to_time_t(now);
	std::tm tm{};
	localtime_s(&tm, &tt);

	std::ostringstream name;
	name << "ida_xref_comments." << std::put_time(&tm, "%Y%m%d.%H%M%S") << ".py";

	std::filesystem::path out = baseDir / name.str();
	std::ofstream ofs(out, std::ios::out | std::ios::trunc);
	if (!ofs.is_open()) return false;

	HMODULE hMod = GetModuleHandleW(nullptr);
	uintptr_t imageBase = reinterpret_cast<uintptr_t>(hMod);

	// Generate IDA script that adds comments with string references
	ofs << "# IDA Pro String XREF Comments\n";
	ofs << "# Generated by SapphireHook StringXrefAnalyzer\n";
	ofs << "# Functions with strings: " << m_state.rows.size() << "\n\n";
	ofs << "import idaapi\n";
	ofs << "import idc\n\n";
	ofs << "# Runtime image base: 0x" << std::hex << imageBase << "\n";
	ofs << "ida_base = idaapi.get_imagebase()\n";
	ofs << "runtime_base = 0x" << std::hex << imageBase << "\n\n";
	ofs << "def rebase(addr):\n";
	ofs << "    return addr - runtime_base + ida_base\n\n";
	ofs << "commented = 0\n";
	ofs << "failed = 0\n\n";

	// Just add comments with the actual strings found
	for (const auto& r : m_state.rows) {
		ofs << "try:\n";
		ofs << "    addr = rebase(0x" << std::hex << r.addr << ")\n";

		// Build comment from strings
		ofs << "    comment = \"XREF Strings: ";
		for (size_t i = 0; i < r.strings.size(); ++i) {
			if (i) ofs << " | ";
			ofs << SanitizeForPython(r.strings[i]);
		}
		ofs << "\"\n";

		ofs << "    idc.set_func_cmt(addr, comment, 0)\n";  // Set as regular comment
		ofs << "    print(f\"Added comment at 0x{addr:X}\")\n";
		ofs << "    commented += 1\n";
		ofs << "except:\n";
		ofs << "    failed += 1\n\n";
	}

	ofs << "print(f\"\\nCompleted: {commented} comments added, {failed} failed\")\n";
	ofs.close();

	outPath = out.string();
	LogInfo("IDA Python script exported to: " + outPath);
	return true;
}

// 2. Export enhanced data with signatures and RVAs
bool StringXrefAnalyzer::ExportEnhancedResults(std::string& outPath) const
{
	if (m_state.rows.empty()) return false;

	std::filesystem::path baseDir = Logger::GetDefaultTempDir();
	std::error_code ec;
	std::filesystem::create_directories(baseDir, ec);

	const auto now = std::chrono::system_clock::now();
	const auto tt = std::chrono::system_clock::to_time_t(now);
	std::tm tm{};
	localtime_s(&tm, &tt);

	std::ostringstream name;
	name << "xref_enhanced." << std::put_time(&tm, "%Y%m%d.%H%M%S") << ".txt";

	std::filesystem::path out = baseDir / name.str();
	std::ofstream ofs(out, std::ios::out | std::ios::trunc);
	if (!ofs.is_open()) {
		LogError(std::string("Export failed: could not open file: ") + out.string());
		return false;
	}

	HMODULE hMod = GetModuleHandleW(nullptr);
	uintptr_t imageBase = reinterpret_cast<uintptr_t>(hMod);

	ofs << "String XREF Analysis - IDA Pro Enhanced Export\n";
	ofs << "===============================================\n\n";
	ofs << "Image Base: 0x" << std::hex << imageBase << std::dec << "\n";
	ofs << "Analysis Time: " << std::put_time(&tm, "%Y-%m-%d %H:%M:%S") << "\n";
	ofs << "Functions: " << m_state.rows.size() << " | ASCII: " << m_state.totalAscii
		<< " | UTF-16: " << m_state.totalUtf16 << "\n\n";

	for (const auto& r : m_state.rows) {
		uintptr_t rva = r.addr - imageBase;

		ofs << "================================================================================\n";
		ofs << "VA:  0x" << std::hex << std::setw(16) << std::setfill('0') << r.addr << "\n";
		ofs << "RVA: 0x" << std::hex << std::setw(8) << std::setfill('0') << rva << "\n";

		// Read first 64 bytes for signature
		uint8_t bytes[64] = { 0 };
		SIZE_T bytesRead = 0;
		if (ReadProcessMemory(GetCurrentProcess(),
			reinterpret_cast<LPCVOID>(r.addr),
			bytes, sizeof(bytes), &bytesRead)) {
			ofs << "Bytes: ";
			for (size_t i = 0; i < std::min(bytesRead, (SIZE_T)32); ++i) {
				ofs << std::hex << std::setw(2) << std::setfill('0')
					<< (int)bytes[i] << " ";
			}
			ofs << "\n";

			// Generate IDA-style pattern (with wildcards for calls/jumps)
			ofs << "Pattern: ";
			for (size_t i = 0; i < std::min(bytesRead, (SIZE_T)32); ++i) {
				// After E8/E9 (CALL/JMP), next 4 bytes are relative
				if (bytes[i] == 0xE8 || bytes[i] == 0xE9) {
					ofs << std::hex << std::setw(2) << std::setfill('0')
						<< (int)bytes[i] << " ? ? ? ? ";
					i += 4; // Skip displacement
				}
				// RIP-relative addressing
				else if (i + 6 < bytesRead && bytes[i] == 0x48 &&
					(bytes[i + 1] == 0x8D || bytes[i + 1] == 0x8B) &&
					(bytes[i + 2] & 0xC7) == 0x05) {
					ofs << std::hex << std::setw(2) << std::setfill('0') << (int)bytes[i] << " ";
					ofs << std::hex << std::setw(2) << std::setfill('0') << (int)bytes[i + 1] << " ";
					ofs << std::hex << std::setw(2) << std::setfill('0') << (int)bytes[i + 2] << " ";
					ofs << "? ? ? ? ";
					i += 6;
				}
				else {
					ofs << std::hex << std::setw(2) << std::setfill('0')
						<< (int)bytes[i] << " ";
				}
			}
			ofs << "\n";
		}

		ofs << std::dec << "String Count: " << r.strings.size() << "\n";
		ofs << "Strings:\n";
		for (const auto& s : r.strings) {
			ofs << "  \"" << s << "\"\n";
		}
		ofs << "\n";
	}

	ofs.close();
	outPath = out.string();
	return true;
}

// 3. Export as CSV for easy spreadsheet analysis
bool StringXrefAnalyzer::ExportToCSV(std::string& outPath) const
{
	if (m_state.rows.empty()) return false;

	std::filesystem::path baseDir = Logger::GetDefaultTempDir();
	std::error_code ec;
	std::filesystem::create_directories(baseDir, ec);

	const auto now = std::chrono::system_clock::now();
	const auto tt = std::chrono::system_clock::to_time_t(now);
	std::tm tm{};
	localtime_s(&tm, &tt);

	std::ostringstream name;
	name << "xref_results." << std::put_time(&tm, "%Y%m%d.%H%M%S") << ".csv";

	std::filesystem::path out = baseDir / name.str();
	std::ofstream ofs(out, std::ios::out | std::ios::trunc);
	if (!ofs.is_open()) return false;

	HMODULE hMod = GetModuleHandleW(nullptr);
	uintptr_t imageBase = reinterpret_cast<uintptr_t>(hMod);

	// CSV header
	ofs << "\"Address\",\"RVA\",\"String Count\",\"Strings\"\n";

	for (const auto& r : m_state.rows) {
		uintptr_t rva = r.addr - imageBase;

		ofs << "\"0x" << std::hex << r.addr << "\",";
		ofs << "\"0x" << std::hex << rva << "\",";
		ofs << std::dec << r.strings.size() << ",\"";

		// Join strings with pipe delimiter
		for (size_t i = 0; i < r.strings.size(); ++i) {
			if (i) ofs << " | ";
			// Escape quotes in CSV
			std::string s = r.strings[i];
			for (char& c : s) {
				if (c == '"') ofs << "\"\"";
				else if (c == '\r' || c == '\n' || c == '\t') c = ' ';
				else ofs << c;
			}
		}
		ofs << "\"\n";
	}

	ofs.close();
	outPath = out.string();
	return true;
}

// 4. Helper to sanitize strings for Python
std::string StringXrefAnalyzer::SanitizeForPython(const std::string& str) const
{
	std::string result;
	for (char c : str) {
		if (c == '"') result += "\\\"";
		else if (c == '\\') result += "\\\\";
		else if (c == '\n') result += "\\n";
		else if (c == '\r') result += "\\r";
		else if (c == '\t') result += "\\t";
		else if (c >= 32 && c <= 126) result += c;
		else {
			// Non-printable as hex escape
			char buf[5];
			std::snprintf(buf, sizeof(buf), "\\x%02x", (unsigned char)c);
			result += buf;
		}
	}
	return result;
}