	// Load .rdata section range
	static bool GetSectionRange(HMODULE mod, const char* name, uint8_t*& begin, size_t& size) {
		auto base = reinterpret_cast<uint8_t*>(mod);
		auto dos = reinterpret_cast<IMAGE_DOS_HEADER*>(base);
		if (dos->e_magic != IMAGE_DOS_SIGNATURE) return false;
		auto nt = reinterpret_cast<IMAGE_NT_HEADERS*>(base + dos->e_lfanew);
		if (nt->Signature != IMAGE_NT_SIGNATURE) return false;
		auto sec = IMAGE_FIRST_SECTION(nt);
		for (unsigned i = 0; i < nt->FileHeader.NumberOfSections; ++i, ++sec) {
			if (std::memcmp(sec->Name, name, (std::min)(static_cast<size_t>(std::strlen(name)), size_t(8))) == 0) {
				begin = base + sec->VirtualAddress;
				size = sec->Misc.VirtualSize ? sec->Misc.VirtualSize : sec->SizeOfRawData;
				return true;
			}
		}
		return false;
	}

	static void CollectAsciiStrings(uint8_t* rdata, size_t rdataSize, size_t minLen, size_t limit,
		std::vector<std::pair<uintptr_t, std::string>>& out) {
		out.reserve((std::min)(limit, size_t(20000)));
		std::string cur;
		for (size_t i = 0; i < rdataSize; ++i) {
			unsigned char c = rdata[i];
			if (c >= 32 && c <= 126) {
				cur.push_back(static_cast<char>(c));
			}
			else {
				if (cur.size() >= minLen) {
					uintptr_t addr = reinterpret_cast<uintptr_t>(rdata + i - cur.size());
					out.emplace_back(addr, cur);
					if (out.size() >= limit) return;
				}
				cur.clear();
			}
		}
		if (cur.size() >= minLen && out.size() < limit) {
			uintptr_t addr = reinterpret_cast<uintptr_t>(rdata + rdataSize - cur.size());
			out.emplace_back(addr, cur);
		}
	}

	static void CollectUtf16Strings(uint8_t* rdata, size_t rdataSize, size_t minLen, size_t limit,
		std::vector<std::pair<uintptr_t, std::string>>& out) {
		size_t added = 0;
		std::string cur;
		size_t i = 0;
		while (i + 1 < rdataSize && added < limit) {
			uint8_t lo = rdata[i];
			uint8_t hi = rdata[i + 1];
			if (hi == 0 && lo >= 32 && lo <= 126) {
				cur.push_back(static_cast<char>(lo));
				i += 2;
			}
			else {
				if (cur.size() >= minLen) {
					uintptr_t addr = reinterpret_cast<uintptr_t>(rdata + i - cur.size() * 2);
					out.emplace_back(addr, cur);
					++added;
				}
				cur.clear();
				i += 2;
			}
		}
		if (cur.size() >= minLen && added < limit) {
			uintptr_t addr = reinterpret_cast<uintptr_t>(rdata + i - cur.size() * 2);
			out.emplace_back(addr, cur);
		}
	}

	// NEW: Sanitize string for embedding in Python comments
	static std::string SanitizePyString(const std::string& s) {
		std::string out; out.reserve(s.size());
		for (char c : s) {
			switch (c) {
			case '\\': out += "\\\\"; break;
			case '\"': out += "\\\""; break;
			case '\n': out += "\\n"; break;
			case '\r': out += "\\r"; break;
			case '\t': out += "\\t"; break;
			default:
				if (c >= 32 && c <= 126) out += c;
				else {
					char buf[5];
					std::snprintf(buf, sizeof(buf), "\\x%02X", (unsigned char)c);
					out += buf;
				}
			}
		}
		return out;
	}

	// NEW: Export IDA Python script that adds repeatable comments (no renaming)
	static bool ExportIdaComments(const std::vector<StringXrefAnalyzer::XrefRow>& rows, std::string& outPath) {
		using SapphireHook::Logger;

		if (rows.empty()) return false;

		std::filesystem::path baseDir = Logger::GetDefaultTempDir();
		std::error_code ec;
		std::filesystem::create_directories(baseDir, ec);

		const auto now = std::chrono::system_clock::now();
		const auto tt = std::chrono::system_clock::to_time_t(now);
		std::tm tm{};
#if defined(_WIN32)
		localtime_s(&tm, &tt);
#else
		tm = *std::localtime(&tt);
#endif
		std::ostringstream name;
		name << "ida_xref_comments." << std::put_time(&tm, "%Y%m%d.%H%M%S") << ".py";

		std::filesystem::path out = baseDir / name.str();
		std::ofstream ofs(out, std::ios::out | std::ios::trunc);
		if (!ofs.is_open()) {
			LogError(std::string("Export failed: could not open file: ") + out.string());
			return false;
		}

		HMODULE hMod = GetModuleHandleW(nullptr);
		uintptr_t imageBase = reinterpret_cast<uintptr_t>(hMod);

		ofs << "# IDA Pro String XREF Comments\n";
		ofs << "# Generated by SapphireHook StringXrefAnalyzer\n";
		ofs << "import idaapi\nimport idc\n\n";
		ofs << "ida_base = idaapi.get_imagebase()\n";
		ofs << "runtime_base = 0x" << std::hex << imageBase << "\n\n";
		ofs << "def rebase(addr):\n    return addr - runtime_base + ida_base\n\n";
		ofs << "commented = 0\nfailed = 0\n\n";

		for (const auto& r : rows) {
			ofs << "try:\n";
			ofs << "    addr = rebase(0x" << std::hex << r.addr << ")\n";
			ofs << "    c = \"XREF Strings: ";
			for (size_t i = 0; i < r.strings.size(); ++i) {
				if (i) ofs << " | ";
				ofs << SanitizePyString(r.strings[i]);
			}
			ofs << "\"\n";
			ofs << "    idc.set_func_cmt(addr, c, 0)\n";
			ofs << "    commented += 1\n";
			ofs << "except Exception as e:\n";
			ofs << "    failed += 1\n\n";
		}
		ofs << "print(f\"Done. {commented} comments added, {failed} failed.\")\n";
		ofs.flush();
		ofs.close();

		outPath = out.string();
		return true;
	}

	// Read printable ASCII string at address (stop at NUL / non-printable), minLen gate
	static bool ReadAsciiString(uintptr_t addr, std::string& out, size_t minLen = 3, size_t maxLen = 512)
	{
		out.clear();
		if (!addr) return false;

		// Quick memory validation
		MEMORY_BASIC_INFORMATION mbi{};
		if (VirtualQuery(reinterpret_cast<LPCVOID>(addr), &mbi, sizeof(mbi)) == 0) return false;
		const DWORD readable = PAGE_READONLY | PAGE_READWRITE | PAGE_WRITECOPY |
			PAGE_EXECUTE_READ | PAGE_EXECUTE_READWRITE | PAGE_EXECUTE_WRITECOPY;
		if (mbi.State != MEM_COMMIT || (mbi.Protect & readable) == 0) return false;

		const char* p = reinterpret_cast<const char*>(addr);
		for (size_t i = 0; i < maxLen; ++i) {
			char c = 0;
			__try { c = p[i]; }
			__except (EXCEPTION_EXECUTE_HANDLER) { return false; }

			if (c == '\0') break;
			if (static_cast<unsigned char>(c) < 32 || static_cast<unsigned char>(c) > 126) return false;
			out.push_back(c);
		}
		return out.size() >= minLen;
	}

	static bool ReadUtf16AsciiString(uintptr_t addr, std::string& out, size_t minLen = 3, size_t maxCP = 512)
	{
		out.clear();
		if (!addr) return false;

		MEMORY_BASIC_INFORMATION mbi{};
		if (VirtualQuery(reinterpret_cast<LPCVOID>(addr), &mbi, sizeof(mbi)) == 0) return false;
		const DWORD readable = PAGE_READONLY | PAGE_READWRITE | PAGE_WRITECOPY |
			PAGE_EXECUTE_READ | PAGE_EXECUTE_READWRITE | PAGE_EXECUTE_WRITECOPY;
		if (mbi.State != MEM_COMMIT || (mbi.Protect & readable) == 0) return false;

		const uint8_t* p = reinterpret_cast<const uint8_t*>(addr);
		for (size_t i = 0; i < maxCP; ++i) {
			uint8_t lo = 0, hi = 0;
			__try {
				lo = p[i * 2 + 0];
				hi = p[i * 2 + 1];
			}
			__except (EXCEPTION_EXECUTE_HANDLER) { return false; }

			// terminator
			if (lo == 0 && hi == 0) break;
			// expect ASCII in UTF-16LE (hi==0)
			if (hi != 0 || lo < 32 || lo > 126) return false;

			out.push_back(static_cast<char>(lo));
		}
		return out.size() >= minLen;
	}

	// Collect strings referenced by RIP-relative instructions within the function window
	static std::vector<std::string> CollectStringsFromFunction(uintptr_t fnStart,
		size_t maxScanBytes = 0x300, size_t minLen = 3)
	{
		std::vector<std::string> out;
		if (!fnStart) return out;

		// PE sections
		HMODULE mod = GetModuleHandleW(nullptr);
		auto rdata = SapphireHook::PatternScanner::GetPESection(mod, ".rdata");
		auto data = SapphireHook::PatternScanner::GetPESection(mod, ".data");

		auto inReadableData = [&](uintptr_t target) -> bool {
			// Check against .rdata or .data
			if (rdata && target >= reinterpret_cast<uintptr_t>(rdata.baseAddress) &&
				target < (reinterpret_cast<uintptr_t>(rdata.baseAddress) + rdata.size))
				return true;
			if (data && target >= reinterpret_cast<uintptr_t>(data.baseAddress) &&
				target < (reinterpret_cast<uintptr_t>(data.baseAddress) + data.size))
				return true;
			return false;
			};

		std::unordered_set<std::string> uniq;
		const std::byte* ip = reinterpret_cast<const std::byte*>(fnStart);
		for (size_t off = 0; off < maxScanBytes; ) {
			uintptr_t target = 0;
			size_t insLen = 0;
			// Use existing instruction helper; returns true for RIP-rel instructions and gives target+length
			if (SapphireHook::PatternScanner::ParseRipRelativeInstruction(ip + off, target, insLen) && insLen > 0) {
				if (inReadableData(target)) {
					std::string s;
					// Try ASCII first, then UTF-16(LE ASCII)
					if (ReadAsciiString(target, s, minLen) || ReadUtf16AsciiString(target, s, minLen)) {
						if (uniq.insert(s).second) out.push_back(std::move(s));
					}
				}
				off += insLen;
			}
			else {
				// Fallback: step 1 byte on unknown instruction
				off += 1;
			}
		}
		return out;
	}

	// Parse address text: supports 0x..., decimal, IDA absolute rebased to runtime, or RVA prefixed with "rva:"
	static bool ParseAddressFlexible(const std::string& txt, uintptr_t& outAddr)
	{
		outAddr = 0;
		if (txt.empty()) return false;

		std::string s = txt;
		// trim
		s.erase(s.begin(), std::find_if(s.begin(), s.end(), [](unsigned char c) { return !std::isspace(c); }));
		s.erase(std::find_if(s.rbegin(), s.rend(), [](unsigned char c) { return !std::isspace(c); }).base(), s.end());

		HMODULE mod = GetModuleHandleW(nullptr);
		const uintptr_t base = reinterpret_cast<uintptr_t>(mod);

		// rva:<hex|dec>
		if (s.rfind("rva:", 0) == 0 || s.rfind("RVA:", 0) == 0) {
			std::string tail = s.substr(4);
			uint64_t v = 0;
			if (tail.rfind("0x", 0) == 0 || tail.rfind("0X", 0) == 0) {
				std::stringstream ss; ss << std::hex << tail.substr(2); ss >> v;
			}
			else {
				std::stringstream ss; ss << tail; ss >> v;
			}
			outAddr = base + static_cast<uintptr_t>(v);
			return outAddr != 0;
		}

		// Hex
		if (s.rfind("0x", 0) == 0 || s.rfind("0X", 0) == 0) {
			uint64_t v = 0;
			std::stringstream ss; ss << std::hex << s.substr(2); ss >> v;
			if (!ss.fail()) {
				outAddr = static_cast<uintptr_t>(v);
				// Rebase IDA abs if needed
				constexpr uintptr_t IDA_BASE = 0x0000000140000000ULL;
				if (outAddr >= IDA_BASE && outAddr < IDA_BASE + 0x20000000ULL) {
					outAddr = (outAddr - IDA_BASE) + base;
				}
				return true;
			}
		}

		// Decimal
		{
			uint64_t v = 0;
			std::stringstream ss; ss << s; ss >> v;
			if (!ss.fail()) {
				outAddr = static_cast<uintptr_t>(v);
				// Also rebase if looks like IDA abs in decimal range
				constexpr uintptr_t IDA_BASE = 0x0000000140000000ULL;
				if (outAddr >= IDA_BASE && outAddr < IDA_BASE + 0x20000000ULL) {
					outAddr = (outAddr - IDA_BASE) + base;
				}
				return true;
			}
		}

		return false;
	}
